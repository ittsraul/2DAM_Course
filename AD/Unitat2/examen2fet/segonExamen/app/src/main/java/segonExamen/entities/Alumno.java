public class Alumno {
    private int idAlumno;
    private int ampa;
    private int clase; 

    public int getIdAlumno() {
        return idAlumno;
    }

    public void setIdAlumno(int idAlumno) {
        this.idAlumno = idAlumno;
    }

    public int getAmpa() {
        return ampa;
    }

    public void setAmpa(int ampa) {
        this.ampa = ampa;
    }

    public int getClase() {
        return clase;
    }

    public void setClase(int clase) {
        this.clase = clase;
    }
   
}
